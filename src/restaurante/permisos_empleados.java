        if (check_admin.isSelected()) {
            administrador = "1";
        } else {
            administrador = "0";
        }
        
        if (check_ventas.isSelected()) {
            ventas = "1";
        } else {
            ventas = "0";
        }
        
        if (check_caja.isSelected()) {
            caja = "1";
        } else {
            caja = "0";
        }
        
        if (check_insumos.isSelected()) {
            insumos = "1";
        } else {
            insumos = "0";
        }
        
        if (check_compras.isSelected()) {
            compras = "1";
        } else {
            compras = "0";
        }
        
        if (check_utilerias.isSelected()) {
            utilerias = "1";
        } else {
            utilerias = "0";
        }
        
        String datos[] = {administrador, ventas, caja, insumos, compras, utilerias, (String) principal.tabla_empleados.getValueAt(principal.tabla_empleados.getSelectedRow(), 0)};
