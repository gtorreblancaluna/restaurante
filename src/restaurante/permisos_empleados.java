    public void traer_permisos() {
//        funcion.conectate();
        String[] colName = {"id_empleado", "administrador", "ventas", "caja", "insumos", "compras", "utilerias"};
        
        dtconduc = funcion.GetTabla(colName, "clientes", "SELECT id_empleado,administrador,ventas,caja,insumos,compras,utilerias FROM empleado WHERE id_empleado =" + (String) principal.tabla_empleados.getValueAt(principal.tabla_empleados.getSelectedRow(), 0) + " ;");
        System.out.println("dtconduc[0][0]: " + dtconduc[0][0]);
        if (dtconduc[0][1].equals("1")) { // es administrador

            check_admin.setSelected(true);
        } else {
            
            if (dtconduc[0][2].equals("1")) { // ventas
                check_ventas.setSelected(true);
            }
            if (dtconduc[0][3].equals("1")) { // caja
                check_caja.setSelected(true);
            }
            if (dtconduc[0][4].equals("1")) { // insumos
                check_insumos.setSelected(true);
            }
            if (dtconduc[0][5].equals("1")) { // compras
                check_compras.setSelected(true);
            }
            if (dtconduc[0][6].equals("1")) { // utilerias
                check_utilerias.setSelected(true);
            }
        }
        
//        funcion.desconecta();
    }

